namespace Tracker.Common.Enums
{
    /// <summary>
    /// Defines the visual style and icon for toast notifications.
    /// </summary>
    public enum ToastType
    {
        /// <summary>
        /// Informational message - uses accent color.
        /// </summary>
        Information,

        /// <summary>
        /// Success message - uses green color.
        /// </summary>
        Success,

        /// <summary>
        /// Warning message - uses amber/yellow color.
        /// </summary>
        Warning,

        /// <summary>
        /// Error message - uses red color.
        /// </summary>
        Error
    }
}

